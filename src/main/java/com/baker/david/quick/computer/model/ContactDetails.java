package com.baker.david.quick.computer.model;

public class ContactDetails {

	private String name;
	private String company;
	private String email;
	private String phone;
	private boolean registeredSupplier;
	private String supplierCode;
	private String issue;
	
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public String getCompany() {
		return company;
	}
	public void setCompany(String company) {
		this.company = company;
	}
	public String getEmail() {
		return email;
	}
	public void setEmail(String email) {
		this.email = email;
	}
	public String getPhone() {
		return phone;
	}
	public void setPhone(String phone) {
		this.phone = phone;
	}
	public boolean isRegisteredSupplier() {
		return registeredSupplier;
	}
	public void setRegisteredSupplier(boolean registeredSupplier) {
		this.registeredSupplier = registeredSupplier;
	}
	public String getSupplierCode() {
		return supplierCode;
	}
	public void setSupplierCode(String supplierCode) {
		this.supplierCode = supplierCode;
	}
	public String getIssue() {
		return issue;
	}
	public void setIssue(String issue) {
		this.issue = issue;
	}
	
	
}
